namespace CplSharp.ConditionActions
{
    /// <summary>
    /// This specifies the condition action type.
    /// </summary>
    public enum ConditionActionType
    {
        /// <summary>
        /// Identifies no condition action type is defined.
        /// </summary>
        Undefined = 0,

        /// <summary>
        /// Identifies the definition will be called when the condition is true.
        /// </summary>
        Definition = 1,
    }
}